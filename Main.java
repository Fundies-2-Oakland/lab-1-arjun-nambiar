public class Main {

    public static class Vector3D{
        double x;
        double y;
        double z;

        public Vector3D(double x, double y, double z){
            this.x = x;
            this.y = y;
            this.z = z;
        }

        public Double getX(){
            return x;
        }

        public Double getY(){
            return y;
        }

        public Double getZ(){
            return z;
        }



    }

}
